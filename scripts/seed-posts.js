async function main() {
  console.log(`총 ${posts.length}개 게시물 삽입 중...`);

  const { data, error } = await supabase.from("posts").insert(posts).select();

  if (error) {
    console.error("삽입 오류:", error);
    process.exit(1);
  }

  console.log(`✅ ${data.length}개 게시물 삽입 완료:`);
  data.forEach((p) =>
    console.log(`  - [${p.constitution_type}] ${p.title} (id: ${p.id})`)
  );
}

main().catch((err) => {
  console.error("오류:", err);
  process.exit(1);
});
